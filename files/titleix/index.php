                        <li class="o-accordion__item"><a class="o-accordion__link" data-parent="#accordion-d14e467" href="index.php#collapse-d14e467-1">SJSU’s Statement of Nondiscrimination</a><ul class="menu vertical nested" id="collapse-d14e467-1">
                              <li class="o-accordion__item--nested">
                                 
                                 <p>The University is dedicated to the principles of equal opportunity and non-discrimination.
                                    These principles permeate all facets of university life: in the admission, housing
                                    and education of students; in policies and practices governing programs of extracurricular
                                    life and activities; and in the employment of faculty, staff, and students. Discrimination
                                    of any kind, including, but not limited to age, ancestry, color, creed, disability,
                                    marital status, medical condition, national origin, pregnancy, race, religion, sex,
                                    or sexual orientation, is an affront to the entire university community and is strictly
                                    prohibited.</p>
                                 </li>
                           </ul>
                        </li>
                        <li class="o-accordion__item"><a class="o-accordion__link" data-parent="#accordion-d14e467" href="index.php#collapse-d14e467-2">SJSU’s Title IX Statement of Nondiscrimination</a><ul class="menu vertical nested" id="collapse-d14e467-2">
                              <li class="o-accordion__item--nested">
                                 
                                 <p>SJSU is committed to maintaining an inclusive community that values diversity and
                                    fosters tolerance and mutual respect. SJSU does not discriminate, and does not tolerate
                                    discrimination, on the basis of sex or gender, which includes gender identity, gender
                                    expression, sexual orientation or sex stereotype in its education programs or activities.
                                    Title IX of the Education Amendments of 1972, and certain other federal and state
                                    laws, prohibit discrimination on the basis of sex in employment, as well as in all
                                    education programs and activities operated by the university (both on and off campus).
                                    The protection against discrimination on the basis of gender or sexual orientation
                                    includes sexual harassment, sexual misconduct, and dating and domestic violence, and
                                    stalking.</p>
                                 </li>
                           </ul>
                        </li>
                        <li class="o-accordion__item"><a class="o-accordion__link" data-parent="#accordion-d14e467" href="index.php#collapse-d14e467-3">What are the Protected Statuses?</a><ul class="menu vertical nested" id="collapse-d14e467-3">
                              <li class="o-accordion__item--nested">
                                 
                                 <p>Please contact us if you want to discuss a problem or need to make a report relating
                                    to discrimination on the basis of a legally protected status, harassment based on
                                    the basis of a legally protected status, sexual misconduct or sexual harassment, or
                                    retaliation because of your opposition to any such conduct, or your participation
                                    in an inquiry or investigation relating to discrimination or harassment.</p>
                                 
                                 <p>Protected statuses, as listed in the CSU Nondiscrimination Policy, and identified
                                    under California and United States law, are:</p>
                                 
                                 <p>Age<br>Disability (physical and mental)<br>Gender (or sex, including sex stereotyping)<br>Gender Identity (including transgender)<br>Gender Expression<br>Genetic Information<br>Marital Status<br>Medical Condition<br>Nationality<br>Pregnancy or Parenting Concerns<br>Race or Ethnicity (including color, caste, or ancestry)<br>Religion (or religious creed)<br>Sexual Orientation, and<br>Veteran or Military Status.</p>
                                 
                                 <p>This website provides more information about reporting and available resources, or
