               <h1 class="o-title--content">Campus Tours</h1>
               <p><img title="Student Ambassadors" src="https://www.sjsu.edu/soar/pics/President%20with%20SAs.jpg" alt="Student Ambassadors"></p>
               
               <p>Welcome to San Jose State University's Campus Tours page! Get ready to explore our
                  vibrant campus, learn about our diverse student community, and discover the many academic
                  opportunities that await you at SJSU.</p>
               
               <h2>Student Ambassador Led Tours</h2>
               
               <p>If you are looking to book a campus tour with us led by our awesome SOAR student ambassador
                  team, please select one of the following: an Individual/Family tour or a School/Group
                  tour.&nbsp;</p>
               
               <h3>Individual and Family Tours</h3>
               
               <p><img title="Student and Family Tour" src="https://www.sjsu.edu/soar/pics/Student%20and%20Family%20Tour.jpg" alt="Student and Family Tour"></p>
               
               <p>Join us for a one-hour Individual and Family Campus Tour, led by our knowledgeable
                  student ambassadors, and experience the vibrant and diverse community of San Jose
                  State University as you explore our beautiful campus, state-of-the-art facilities,
                  and academic programs.</p>
               
               
               <p class="o-c2a o-c2a--content-page"><a class="o-c2a__link" href="https://myvst.co/v/udlaexpuml"><span class="o-c2a__text ou-span-allowed">Register</span></a></p> 
               
               
               <h4>Saturday Tours (Not open to schools/groups)</h4>
               
               <p><img src="https://www.sjsu.edu/soar/pics/tour%20asd%202015-041215-116.jpg" alt="saturday campus tour"></p>
               
               <p>Join us for a one-hour Individual and Family Campus Tour on select Saturdays, led
                  by our knowledgeable student ambassadors, and experience the vibrant and diverse community
                  of San Jose State University as you explore our beautiful campus, state-of-the-art
                  facilities, and academic programs.</p>
               
               
               <p class="o-c2a o-c2a--content-page"><a class="o-c2a__link" href="https://spartans.sjsu.edu/portal/tour"><span class="o-c2a__text ou-span-allowed">Register</span></a></p> 
               
               
               <h3>&nbsp;</h3>
               
               <h3>School and Group Tours</h3>
               
               <p><img title="School Group Tour" src="https://www.sjsu.edu/soar/pics/School%20Group%20Tour.jpg" alt="School Group Tour"></p>
               
               <p>Discover all that San Jose State University has to offer with our School/Group Tour
                  program, designed for educational groups (up to 50 individuals per group). Led by
                  experienced student ambassadors, the outdoor walking tour offers an in-depth look
                  at campus life, academic programs, and our state-of-the-art facilities, with plenty
                  of opportunities to ask questions and explore.</p>
               
               <p>*Unfortunately, we are not currently offering guided tours for elementary and middle
                  school groups. However, K-8 groups are welcome to visit campus, take a self-guided
                  tour, and can pick up materials at our Welcome Center. <a href="campus-tours.php#Library">You can also visit the children and teens center in the Dr. Martin Luther King Jr.
                     Library.</a></p>
               
               
               <p class="o-c2a o-c2a--content-page"><a class="o-c2a__link" href="https://myvst.co/v/roydffdrvf"><span class="o-c2a__text ou-span-allowed">Register</span></a></p> 
               
               
               <h2>&nbsp;</h2>
               
               <h2>Self-Guided Tours</h2>
               
               <p><img title="self guided tour" src="https://www.sjsu.edu/soar/pics/tour%20self%20guided.jpg" alt="self guided tour"></p>
               
               <p>Embark on a self-guided walking tour of San Jose State University's vibrant campus
                  and explore at your own pace. The tour provides an excellent opportunity to discover
                  the campus's history, academic programs, and state-of-the-art facilities while following
                  a designated route that includes significant campus landmarks. You can visit the Welcome
                  Center and embark on a personalized journey to learn more about what SJSU has to offer.</p>
               
               
               <p class="o-c2a o-c2a--content-page"><a class="o-c2a__link" href="https://www.sjsu.edu/soar/docs/Printable%20SJSU%20Self%20Guided%20Campus%20Tour%202023.pdf">Self Guided Tour Map [pdf]</a></p> 
               
               
